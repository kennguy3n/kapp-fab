import {
  forwardRef,
  type ComponentPropsWithoutRef,
  type ElementRef,
  type HTMLAttributes,
} from "react";
import * as DropdownMenuPrimitive from "@radix-ui/react-dropdown-menu";
import { cn } from "../lib/cn";

/**
 * DropdownMenu wraps Radix DropdownMenu.  The Radix primitives
 * handle:
 *
 *   - keyboard navigation (arrow keys + Home/End + typeahead);
 *   - focus management (trap inside menu, restore to trigger
 *     on close);
 *   - submenu open/close on focus (not just click) so keyboard
 *     users can traverse nested menus;
 *   - aria-haspopup / aria-expanded / role=menu wiring.
 *
 * We expose every Radix subcomponent (Item / CheckboxItem /
 * RadioItem / Label / Separator / Sub / SubTrigger /
 * SubContent) styled to match the design system.  Using the
 * full surface area instead of just <Item> is intentional —
 * dropdown menus in this app commonly have checkable options
 * (toggle column visibility), grouped sections, and submenus.
 */
export const DropdownMenu = DropdownMenuPrimitive.Root;
export const DropdownMenuTrigger = DropdownMenuPrimitive.Trigger;
export const DropdownMenuGroup = DropdownMenuPrimitive.Group;
export const DropdownMenuPortal = DropdownMenuPrimitive.Portal;
export const DropdownMenuSub = DropdownMenuPrimitive.Sub;
export const DropdownMenuRadioGroup = DropdownMenuPrimitive.RadioGroup;

export const DropdownMenuSubTrigger = forwardRef<
  ElementRef<typeof DropdownMenuPrimitive.SubTrigger>,
  ComponentPropsWithoutRef<typeof DropdownMenuPrimitive.SubTrigger> & {
    inset?: boolean;
  }
>(({ className, inset, children, ...props }, ref) => (
  <DropdownMenuPrimitive.SubTrigger
    ref={ref}
    className={cn(
      "flex cursor-default select-none items-center gap-2 rounded-sm px-2 py-1.5 text-sm outline-none",
      "focus:bg-bg-subtle data-[state=open]:bg-bg-subtle",
      inset && "pl-8",
      className,
    )}
    {...props}
  >
    {children}
    <svg
      aria-hidden="true"
      className="ml-auto h-4 w-4"
      viewBox="0 0 24 24"
      fill="none"
      stroke="currentColor"
      strokeWidth="2"
      strokeLinecap="round"
      strokeLinejoin="round"
    >
      <polyline points="9 18 15 12 9 6" />
    </svg>
  </DropdownMenuPrimitive.SubTrigger>
));
DropdownMenuSubTrigger.displayName =
  DropdownMenuPrimitive.SubTrigger.displayName;

