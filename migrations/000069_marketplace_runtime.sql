-- ---------------------------------------------------------------
-- 5. marketplace_dispatch_log
-- ---------------------------------------------------------------

CREATE TABLE IF NOT EXISTS marketplace_dispatch_log (
    id                  UUID NOT NULL DEFAULT gen_random_uuid(),
    tenant_id           UUID NOT NULL REFERENCES tenants(id) ON DELETE CASCADE,
    -- SET NULL so the audit row survives uninstall. The
    -- (extension_id, version_id) tuple is also captured so an
    -- ops tool can correlate after the installation row goes
    -- away.
    installation_id     UUID REFERENCES marketplace_extension_installations(id) ON DELETE SET NULL,
    extension_id        UUID NOT NULL,
    extension_version_id UUID NOT NULL,
    kind                TEXT NOT NULL,
    endpoint            TEXT NOT NULL,
    request_id          UUID NOT NULL,
    attempt             INTEGER NOT NULL,
    -- Body hash, not body. Audit log is high-volume; the operator
    -- can re-execute against the live extension if the body is
    -- needed for replay-debugging. Hash is enough for forensics
    -- ("did we dispatch THIS payload?").
    request_body_sha256 TEXT NOT NULL,
    signature           TEXT NOT NULL,
    response_status     INTEGER,
    response_latency_ms INTEGER,
    error               TEXT,
    started_at          TIMESTAMPTZ NOT NULL DEFAULT now(),
    completed_at        TIMESTAMPTZ,

    PRIMARY KEY (tenant_id, id),

    CONSTRAINT marketplace_dispatch_log_kind_chk
        CHECK (kind IN (
            'tool_invoke',
            'lifecycle_pre_install',
            'lifecycle_post_install',
            'lifecycle_pre_uninstall',
            'lifecycle_post_uninstall',
            'event_delivery',
            'health_check'
        )),
    CONSTRAINT marketplace_dispatch_log_attempt_positive_chk
        CHECK (attempt >= 1),
    CONSTRAINT marketplace_dispatch_log_body_sha256_chk
        CHECK (request_body_sha256 ~ '^[0-9a-f]{64}$'),
    -- Either we have a response status OR we have an error. Both
    -- can be present (HTTP 502 with the underlying TCP-reset
    -- error text). Neither isn't allowed once completed_at is
    -- set; while completed_at is NULL the row is the in-flight
    -- attempt and both columns are NULL.
    CONSTRAINT marketplace_dispatch_log_completion_consistent_chk
        CHECK (
            (completed_at IS NULL AND response_status IS NULL AND error IS NULL AND response_latency_ms IS NULL)
            OR (completed_at IS NOT NULL AND (response_status IS NOT NULL OR error IS NOT NULL))
        )
);

-- Group all attempts for one logical dispatch (e.g. retry trail).
CREATE INDEX IF NOT EXISTS marketplace_dispatch_log_request_idx
    ON marketplace_dispatch_log (tenant_id, request_id, attempt);

-- Recent-failure lookup for the health check surface.
CREATE INDEX IF NOT EXISTS marketplace_dispatch_log_failure_idx
    ON marketplace_dispatch_log (tenant_id, installation_id, started_at DESC)
    WHERE response_status IS NOT NULL AND response_status >= 400;

ALTER TABLE marketplace_dispatch_log ENABLE ROW LEVEL SECURITY;
ALTER TABLE marketplace_dispatch_log FORCE ROW LEVEL SECURITY;

DROP POLICY IF EXISTS marketplace_dispatch_log_isolation
    ON marketplace_dispatch_log;
CREATE POLICY marketplace_dispatch_log_isolation
    ON marketplace_dispatch_log
    USING (tenant_id = NULLIF(current_setting('app.tenant_id', true), '')::uuid)
    WITH CHECK (tenant_id = NULLIF(current_setting('app.tenant_id', true), '')::uuid);

GRANT SELECT, INSERT, UPDATE ON marketplace_dispatch_log TO kapp_app;
