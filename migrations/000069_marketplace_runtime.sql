-- ---------------------------------------------------------------
-- 3. marketplace_extension_agent_tools
-- ---------------------------------------------------------------

CREATE TABLE IF NOT EXISTS marketplace_extension_agent_tools (
    tenant_id           UUID NOT NULL REFERENCES tenants(id) ON DELETE CASCADE,
    installation_id     UUID NOT NULL REFERENCES marketplace_extension_installations(id) ON DELETE CASCADE,
    tool_name           TEXT NOT NULL,
    descriptor          JSONB NOT NULL,
    handler             TEXT NOT NULL,
    endpoint            TEXT NOT NULL,
    timeout_ms          INTEGER NOT NULL,
    retry_max_attempts  INTEGER NOT NULL,
    retry_backoff       TEXT NOT NULL,
    registered_at       TIMESTAMPTZ NOT NULL DEFAULT now(),

    PRIMARY KEY (tenant_id, installation_id, tool_name),

    -- handler enum mirrors the manifest's accepted values. wasm
    -- is reserved for Phase 2 — the runtime rejects it until then.
    CONSTRAINT marketplace_ext_agent_tools_handler_chk
        CHECK (handler IN ('webhook')),
    -- endpoint MUST be https — same rule as installations.webhook_base.
    CONSTRAINT marketplace_ext_agent_tools_endpoint_https_chk
        CHECK (endpoint ~ '^https://'),
    CONSTRAINT marketplace_ext_agent_tools_timeout_positive_chk
        CHECK (timeout_ms > 0 AND timeout_ms <= 30000),
    CONSTRAINT marketplace_ext_agent_tools_attempts_chk
        CHECK (retry_max_attempts >= 1 AND retry_max_attempts <= 5),
    CONSTRAINT marketplace_ext_agent_tools_backoff_chk
        CHECK (retry_backoff IN ('linear', 'exponential'))
);

CREATE INDEX IF NOT EXISTS marketplace_ext_agent_tools_install_idx
    ON marketplace_extension_agent_tools (tenant_id, installation_id);

ALTER TABLE marketplace_extension_agent_tools ENABLE ROW LEVEL SECURITY;
ALTER TABLE marketplace_extension_agent_tools FORCE ROW LEVEL SECURITY;

