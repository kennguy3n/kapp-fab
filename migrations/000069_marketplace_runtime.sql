-- ---------------------------------------------------------------
-- 4. marketplace_webhook_subscriptions
-- ---------------------------------------------------------------

CREATE TABLE IF NOT EXISTS marketplace_webhook_subscriptions (
    id                UUID NOT NULL DEFAULT gen_random_uuid(),
    tenant_id         UUID NOT NULL REFERENCES tenants(id) ON DELETE CASCADE,
    installation_id   UUID NOT NULL REFERENCES marketplace_extension_installations(id) ON DELETE CASCADE,
    event             TEXT NOT NULL,
    filter            JSONB NOT NULL DEFAULT '{}'::jsonb,
    endpoint          TEXT NOT NULL,
    registered_at     TIMESTAMPTZ NOT NULL DEFAULT now(),

    PRIMARY KEY (tenant_id, id),

    -- event names follow the kapp event-bus convention (lowercase
    -- dotted segments). Same shape the workflow engine uses for
    -- emit_event / on_event hooks.
    CONSTRAINT marketplace_webhook_subs_event_chk
        CHECK (event ~ '^[a-z][a-z0-9_]*(\.[a-z][a-z0-9_]*)+$'),
    CONSTRAINT marketplace_webhook_subs_endpoint_https_chk
        CHECK (endpoint ~ '^https://')
);

CREATE INDEX IF NOT EXISTS marketplace_webhook_subs_install_idx
    ON marketplace_webhook_subscriptions (tenant_id, installation_id);

-- Lookup index for the B4 router: given an event-type, find every
-- subscription. The router scans this on each drain tick.
CREATE INDEX IF NOT EXISTS marketplace_webhook_subs_event_idx
    ON marketplace_webhook_subscriptions (tenant_id, event);

