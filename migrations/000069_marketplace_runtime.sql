-- Phase B3 — Marketplace runtime engine schema
--
-- Extends the B2 catalog (000068) with the FIVE runtime-tracking
-- tables the engine needs to drive install / invoke / uninstall:
--
--   1. marketplace_extension_ktypes
--      One row per extension-authored KType registered for a given
--      install. Cascades on installation delete so an uninstall
--      cleanly removes every ext.publisher.name KType the extension
--      brought in (extension KTypes live alongside platform KTypes
--      in the krecords table but are isolated to the installation
--      that registered them).
--
--   2. marketplace_extension_workflows
--      Same shape for state-machine definitions. The platform's
--      built-in `workflows` table predates RLS-by-FORCE so we use
--      a separate table for extension-authored workflows to keep
--      the FORCE-RLS posture B2 introduced for installations
--      uniform across the marketplace surface.
--
--   3. marketplace_extension_agent_tools
--      Agent-tool descriptors with handler/endpoint/timeout/retry
--      MATERIALIZED as columns (not just stored as JSONB inside
--      the descriptor blob) so the runtime dispatcher does a single
--      indexed point-read per invoke instead of re-parsing the
--      manifest. The descriptor JSON is preserved as well for the
--      agent platform's introspection surfaces (B4) — the
--      materialised columns are an index, not the source of truth.
--
--   4. marketplace_webhook_subscriptions
--      One row per `webhooks_consumed[]` entry in the manifest.
--      The B4 event-router will SELECT from this table on every
--      drain tick (same shape as the platform's existing
--      `webhooks` table from 000024) — the marketplace table is
--      kept separate so the runtime audit + cascade semantics
--      stay aligned with the rest of the extension surface.
--
--   5. marketplace_dispatch_log
--      Append-only audit log of every outbound HMAC POST the
--      runtime sends (tool invokes + lifecycle hooks). Survives
--      installation deletion via ON DELETE SET NULL on
--      installation_id so post-uninstall the log is still
--      readable for forensics. Indexed by (tenant_id, request_id)
--      so a single invocation's retry attempts can be grouped
--      with one query.
--
-- ALSO adds the per-install HMAC signing secret column to
-- marketplace_extension_installations. The secret is 32 random
-- bytes (base64url-encoded → 43 chars + no padding) generated by
-- the engine at install time. The reverse direction — extension's
-- webhook server verifying the signature on incoming requests —
-- uses this same secret.
--
-- Conventions matching 000068:
--   * tenant_id UUID NOT NULL REFERENCES tenants(id) ON DELETE CASCADE
--   * ENABLE ROW LEVEL SECURITY + FORCE ROW LEVEL SECURITY
--   * tenant_isolation policy keyed off current_setting('app.tenant_id')
--   * GRANT SELECT/INSERT/UPDATE/DELETE to kapp_app

-- ---------------------------------------------------------------
-- Signing secret on installations
-- ---------------------------------------------------------------

-- The signing secret is base64url, no padding, 32 bytes of entropy
-- → 43 chars. NOT NULL with a sentinel default so existing test-
-- harness installations (created via direct SQL in the integration
-- tests of B2) keep working. The runtime regenerates the secret on
-- every install via runtime.Engine.Install so the default is only
-- ever observed by tests that bypass the engine.
ALTER TABLE marketplace_extension_installations
    ADD COLUMN IF NOT EXISTS signing_secret TEXT NOT NULL DEFAULT '';

-- After application code (B3+) populates the column on every new
-- install, future migrations may drop the default. We keep it for
-- this migration because some integration-test fixtures predate B3
-- and insert directly into the table.
--
-- ADD CONSTRAINT does not have an IF NOT EXISTS form; we wrap in a
-- DO block so re-running the migration (e.g. after a previous
-- partial failure on a later statement) is a no-op rather than
-- erroring with 42710 "constraint already exists".
DO $$
BEGIN
    IF NOT EXISTS (
        SELECT 1 FROM pg_constraint
        WHERE conname = 'marketplace_installations_signing_secret_format_chk'
    ) THEN
        ALTER TABLE marketplace_extension_installations
            ADD CONSTRAINT marketplace_installations_signing_secret_format_chk
            CHECK (
                signing_secret = ''
                OR signing_secret ~ '^[A-Za-z0-9_-]{43}$'
            );
    END IF;
END $$;

-- ---------------------------------------------------------------
-- 1. marketplace_extension_ktypes
-- ---------------------------------------------------------------

CREATE TABLE IF NOT EXISTS marketplace_extension_ktypes (
    tenant_id        UUID NOT NULL REFERENCES tenants(id) ON DELETE CASCADE,
    installation_id  UUID NOT NULL REFERENCES marketplace_extension_installations(id) ON DELETE CASCADE,
    ktype_name       TEXT NOT NULL,
    ktype_version    INTEGER NOT NULL DEFAULT 1,
    schema           JSONB NOT NULL,
    registered_at    TIMESTAMPTZ NOT NULL DEFAULT now(),

    PRIMARY KEY (tenant_id, installation_id, ktype_name),

    -- Extension KType names live in the ext.<publisher>.<slug>
    -- namespace per EXTENSION_SPEC §4. The DB CHECK mirrors the
    -- Go-side regex in internal/marketplace/manifest.go so
    -- malformed names cannot reach the table via a direct INSERT
    -- (e.g. from a future ops script that bypasses the engine).
    CONSTRAINT marketplace_ext_ktypes_name_chk
        CHECK (ktype_name ~ '^ext\.[a-z][a-z0-9_-]*\.[a-z][a-z0-9_]*$'),
    CONSTRAINT marketplace_ext_ktypes_version_positive_chk
        CHECK (ktype_version >= 1)
);

CREATE INDEX IF NOT EXISTS marketplace_ext_ktypes_install_idx
    ON marketplace_extension_ktypes (tenant_id, installation_id);

ALTER TABLE marketplace_extension_ktypes ENABLE ROW LEVEL SECURITY;
ALTER TABLE marketplace_extension_ktypes FORCE ROW LEVEL SECURITY;

DROP POLICY IF EXISTS marketplace_extension_ktypes_isolation
    ON marketplace_extension_ktypes;
CREATE POLICY marketplace_extension_ktypes_isolation
    ON marketplace_extension_ktypes
    USING (tenant_id = NULLIF(current_setting('app.tenant_id', true), '')::uuid)
    WITH CHECK (tenant_id = NULLIF(current_setting('app.tenant_id', true), '')::uuid);

GRANT SELECT, INSERT, UPDATE, DELETE ON marketplace_extension_ktypes TO kapp_app;

